#include "Util.h"



namespace Lab {
namespace Util {

unsigned int
numberOfDigits(unsigned int value)
{
	if (value == 0) {
		THROW_EXCEPTION(InvalidParameterException, "Value must not be zero.");
	}

	return static_cast<unsigned int>(std::floor(std::log10(static_cast<double>(value))) + 1.0);
}

} // namespace Util
} // namespace Lab
